Within the Four Pillars, the month is the pillar representing information 
about the person's parents or later years in life. Many Chinese astrologers 
consider the month pillar to be the most important one in determining the 
circumstances of one's adult life.

The 12 animals are also linked to the traditional Chinese agricultural 
calendar, which runs alongside the better known lunar calendar. Instead of 
months, this calendar is divided into 24 two week segments known as Solar 
Terms. Each animal is linked to two of these solar terms for a period similar 
to the Western month. Unlike the 60 year lunar calendar, which can vary by as 
much as a month in relation to the Western calendar, the agricultural calendar 
varies by only one day, beginning on the Western February 3 or 4 every year. 
Again unlike the cycle of the lunar years, which begins with the Rat, the 
agricultural calendar begins with the Tiger as it is the first animal of spring.

As each sign is linked to a month of the solar year, it is thereby also linked 
to a season. Each of the elements is also linked to a season (see above), and 
the element that shares a season with a sign is known as that sign's fixed 
element. In other words, that element is believed to impart some of its 
characteristics to the sign concerned. The fixed element of each sign applies 
also to the year and hour signs, and not just the monthly sign. It is important 
to note that the fixed element is separate from the cycle of elements which 
interact with the signs in the 60 year cycle.Season	

