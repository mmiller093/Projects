The Four Animal Trines

The first trine consists of the Rat, Dragon, and Monkey. These three signs are 
intense and powerful individuals, capable of great good or great evil. They 
make great leaders, but the three may have different approaches. Frustrated 
when hampered, these signs are ruled by highly potent energy and 
unpredictability. At their worst, Rats are ruthlessly power-hungry, vindictive, 
and Machiavellian; Dragons are inflexible megalomaniacs and narcissists; and 
Monkeys are destructive manipulators and hedonists. They are intelligent, 
magnanimous, charismatic, charming, authoritative, confident, eloquent and 
artistic. They can also be tyrannical, bombastic, prejudiced, deceitful, 
imperious, ruthless, power-hungry, and megalomaniacal.

The second trine consists of the Ox, Snake, and Rooster. These three soul 
mates conquer life through endurance, application, and slow accumulation of 
energy. Although each sign is fixed and rigid in opinions and views, they are 
geniuses in the art of meticulous planning. They are hardworking, discreet, 
modest, industrious, charitable, loyal, punctual, philosophical, patient, and 
good-hearted individuals with high moral standards. They can also be 
self-righteous, vain, critical, judgmental, myopic, narrow-minded, and petty. 
They are also natural leaders, but are better natured than the first trine.

The third trine consists of the Tiger, Horse, and Dog. These three signs seek 
a true lover, and are like-minded in their pursuit of humanitarian causes. 
Each is a gifted orator and excels at verbal communication. Relationships and 
personal contact are of highest priority and each one seeks their intimate soul 
mate. Idealistic and impulsive, the Tiger, Horse and Dog follow the beat of 
their own drummer. Defiant against injustice, these three signs wilt without 
large amounts of physical affection and loyal support for causes. They are 
productive, enthusiastic, independent, engaging, dynamic and honourable. They 
can also be rash, rebellious, quarrelsome, hot-headed, reckless, anxious, 
moody, disagreeable, and stubborn. Although these three signs are loyal, they 
can be very protective when lied to. The three signs do not enjoy being told 
what to do, but will listen when it is a person they love or trust 
whole-heartedly.

The fourth trine consists of the Rabbit, Ram and Pig. The quest for these 
three signs is the aesthetic and beautiful in life. Their calm nature gives 
them great leadership abilities. They are artistic, refined, intuitive, and 
well-mannered. These souls love the preliminaries in love, and are fine artists 
in their lovemaking. The Rabbit, Ram (Sheep) and Pig have been bestowed with 
calmer natures than the other 9 signs. These three are compassionately aware, 
yet detached and resigned to their condition. They seek beauty and a sensitive 
lover. They are caring, unique, self-sacrificing, obliging, sensible, creative, 
emphatic, tactful, and prudent. They can also be naive, pedantic, insecure, 
cunning, indecisive, and pessimistic.

The order of the trines names (first, second, third, and fourth) are equal to 
the order of where they are placed to the highest honor. The first being the 
highest and the fourth being the lowest

The 12 Zodiac animal signs (?? shengxiao) are, in order, the rat, ox, tiger, 
rabbit, dragon, snake, horse, sheep (ram or goat), monkey, rooster, dog, and 
pig. There are many legends to explain the beginning of the zodiac. One of the 
most popular reads, in summarised form, as follows:

The rat was given the task of inviting the animals to report to the Jade 
Emperor for a banquet to be selected for the zodiac signs. The cat was a good 
friend of the rat, but the rat tricked him into believing that the banquet was 
the next day. The cat slept through the banquet, thinking that it was the next 
day. When he found out, the cat vowed to be the rat's natural enemy for ages 
to come.

Interestingly, the cat does make it into the Vietnamese Zodiac, in place of the 
rabbit.

Another popular legend has it that a race was used to decide the animals to 
report to the Jade Emperor.

The cat and the rat were the worst swimmers in the animal kingdom. Although 
bad swimmers, they were both intelligent. They decided that the best and 
fastest way to cross the river was to hop on the back of the ox. The ox, being 
a naïve and good-natured animal, agreed to carry them across. However, overcome 
with a fierce competitiveness, the rat decided that in order to win, it must do 
something and promptly pushed the cat into the river. Because of this, the cat 
has never forgiven the rat, and hates the water as well. After the ox had 
crossed the river, the rat jumped ahead and reached the shore first, and it 
claimed first place in the competition.

Following closely behind was the strong ox, and it was named the 2nd animal in 
the zodiac. After the ox, came the tiger, panting, while explaining to the Jade 
Emperor just how difficult it was to cross the river with the heavy currents 
pushing it downstream all the time. But with powerful strength, it made to 
shore and was named the 3rd animal in the cycle.

Suddenly, from a distance came a thumping sound, and the rabbit arrived. It 
explained how it crossed the river: by jumping from one stone to another in a 
nimble fashion. Halfway through, it almost lost the race but the rabbit was 
lucky enough to grab hold of a floating log that later washed him to shore. 
For that, it became the 4th animal in the zodiac cycle. Coming in 5th place was 
the dragon, flying. Of course, the Jade Emperor was deeply curious as to why a 
strong and flying creature such as the dragon should fail to reach first. The 
mighty dragon explained that he had to stop and make rain to help all the 
people and creatures of the earth, and therefore he was held back a little. 
Then, on his way to the finish line, he saw a little helpless rabbit clinging 
onto a log so he did a good deed and gave a puff of breath to the poor creature 
so that it could land on the shore. The Jade Emperor was very pleased with the 
actions of the dragon, and he was added into the zodiac cycle. As soon as he 
had done so, a galloping sound was heard, and the horse appeared. Hidden on the 
horse's hoof is the snake, whose sudden appearance gave the horse a fright, 
thus making it fall back and giving the snake the 6th spot, while the horse 
placed 7th.

Not long after that, a little distance away, the sheep, monkey, and rooster 
came to the shore. These three creatures helped each other to get to where 
they are. The rooster spotted a raft, and took the other two animals with it. 
Together, the ram and the monkey cleared the weeds, tugged and pulled and 
finally got the raft to the shore. Because of their combined efforts, the 
Emperor was very pleased and promptly named the ram as the 8th creature, the 
monkey as the 9th, and the rooster the 10th.

The 11th animal is the dog. His explanation for being late—although he was s
upposed to be the best swimmer amongst the rest—was that he needed a good bath 
after a long spell, and the fresh water from the river was too big a 
temptation. For that, he almost didn't make it to finish line. Just as the 
Jade Emperor was about to call it a day, an oink and squeal was heard from a 
little pig. The pig got hungry during the race, promptly stopped for a feast 
and then fell asleep. After the nap, the pig continued the race and was named 
the 12th and last animal of the zodiac cycle. The cat finished too late 
(thirteenth) to win any place in the calendar, and vowed to be the enemy of 
the rat forevermore.

In Buddhism, legend has it that Buddha summoned all of the animals of the 
earth to come before him before his departure from this earth, but only twelve 
animals actually came to bid him farewell. To reward the animals who came to 
him, he named a year after each of them. The years were given to them in the 
order they had arrived.

