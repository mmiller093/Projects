A different animal rules each day. Within the Four Pillars, the day is the 
pillar representing information about the person him/herself - his/her adult 
and married life.

The Chinese zodiac is also used to label times of day, with each sign 
corresponding to a "large-hour" or shichen (??), which is a two-hour period 
(24 divided by 12 animals). It is therefore important to know the exact time 
of birth to determine it. The secret animal is thought to be a person’s truest 
representation, since this animal is determined by the smallest denominator: a 
person’s birth hour. As this sign is based on the position of the sun in the 
sky and not the time of your local clock, it is important to compensate for 
daylight saving time. However, some online systems already compensate for 
daylight saving time, and astrologers may compensate your time for you 
oblivious to the fact that you've compensated it yourself, leading to an 
inaccurate reading.

