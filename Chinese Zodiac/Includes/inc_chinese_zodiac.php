In Chinese astrology the animal signs assigned by year represent what others 
perceive you as being or how you present yourself. It is a common misconception 
that the animals assigned by year are the only signs, and many western 
descriptions of Chinese astrology draw solely on this system. In fact, there 
are also animal signs assigned by month (called inner animals) and hours of the 
day (called secret animals).

To sum it up, while a person might appear to be a dragon because they were born 
in the year of the dragon, they might also be a snake internally and an ox 
secretively. In total, this makes for 8,640 possible combinations (five 
elements x 12 animals in the 60 year cycle (12 x 5 = 60) , 12 months, 12 times 
of day) that a person might be. These are all considered critical for the 
proper use of Chinese astrology.

Due to confusion with synonyms during translation, some of the animals 
depicted by the English words did not exist in ancient China. For example, 
? can mean both goat and sheep, but goat is the species that existed in central 
China before sheep, and goat is the species seen in illustrations, not sheep. 
Similarly, ? (rat) can also be translated as mouse, as there are no distinctive 
words for the two genera in Chinese. Further, ? (pig) is sometimes translated 
to boar after its Japanese name, and ? (water buffalo) is commonly thought to 
be ox.

The Four Pillars method can be traced back to the Han Dynasty (206 BC-220 AD), 
and is still much used in Feng Shui astrology and general analysis today. The 
Four Pillars or Columns chart is called such as the Chinese writing causes it 
to fall into columns. Each pillar or column contains a stem and a branch - and 
each column relates to the year, month, day and hour of birth. The first column 
refers to the year animal and element, the second to the month animal and 
element, the third to the day animal and element, and the last to the hour 
animal and element. Within the 'Four Pillars', the Year column is the 
information about your ancestor or early age. The Month column is the 
information about your parents or growing age. The Day column is the 
information about you (upper character) and your spouse (lower character) or 
adult age. The Hour column is the information about kids or late age. Because 
the Day column represents you, you are black cow (water cow), not brown tiger 
from year.

Within the Four Pillars, the year is the pillar representing information about 
the person's ancestry or early age.

