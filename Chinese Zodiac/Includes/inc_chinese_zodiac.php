<p>The Sheng xiao (Chinese: ??; pinyin: sh?ngxiào), better known as Chinese Zodiac, 
are 12 animals which are representative of years in some East Asian countries. 
Each year of the 12-year cycle is named after one of the original 12 animals. 
Each animal has a different personality and different characteristics.

Despite being called “Chinese zodiac”, it is not a variation of zodiac. The 
Chinese zodiac refers to a pure calendrical cycle; there are no equivalent 
constellations like those of the occidental zodiac.

Chinese zodiac signs represent twelve different types of personalities. The 
zodiac traditionally begins with the sign of the Rat, and there are many 
stories about the Origins of the Chinese Zodiac which explain why this is so 
(see below). The following are the twelve zodiac signs in order and their 
characteristics.

